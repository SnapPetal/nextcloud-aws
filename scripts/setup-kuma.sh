echo "  To expose Kuma externally, add a Nginx proxy pass. For example:"
echo ""
echo "  # In your Nginx config, add a new server block or location:"
echo "  server {"
echo "      server_name status.thonbecker.biz;"
echo "      location / {"
echo "          proxy_pass http://127.0.0.1:3001/;"
echo "          proxy_set_header Host \$host;"
echo "          proxy_set_header X-Real-IP \$remote_addr;"
echo "          proxy_http_version 1.1;"
echo "          proxy_set_header Upgrade \$http_upgrade;"
echo "          proxy_set_header Connection \"upgrade\";"
echo "      }"
echo "  }"
echo ""
echo -e "${GREEN}Kuma is running at http://127.0.0.1:3001${NC}"
