echo ""
echo -e "${YELLOW}Step 1: Initial Setup${NC}"
echo "  Open http://localhost:3001 (or via SSH tunnel) and create an admin account."
echo "  SSH tunnel: ssh -L 3001:127.0.0.1:3001 user@your-server"
echo ""
echo -e "${YELLOW}Step 2: Add Monitors${NC}"
echo "  Recommended monitors to add:"
echo ""
echo "  Nextcloud:"
echo "    - Type: HTTP(s)"
echo "    - URL: http://nextcloud-app/status.php"
echo "    - Expected keyword: \"installed\":true"
echo ""
echo "  MariaDB:"
echo "    - Type: Docker Container"
echo "    - Container: nextcloud-db"
echo ""
echo "  Redis:"
echo "    - Type: Docker Container"
echo "    - Container: nextcloud-redis"
echo ""
echo "  ClamAV:"
echo "    - Type: Docker Container"
echo "    - Container: nextcloud-clamav"
echo ""
echo "  Ente Photos:"
echo "    - Type: HTTP(s)"
echo "    - URL: http://ente-web:3000"
echo "    - Name: Ente Web"
echo ""
echo "  Ente API:"
echo "    - Type: HTTP(s)"
echo "    - URL: http://ente-museum:8080/ping"
echo "    - Name: Ente API"
echo ""
echo "  Ente Postgres:"
echo "    - Type: Docker Container"
echo "    - Container: ente-postgres"
echo ""
